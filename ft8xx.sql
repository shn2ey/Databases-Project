CREATE TABLE IF NOT EXISTS users (user_ID INT NOT NULL, first_name VARCHAR(40), last_name VARCHAR(40), address VARCHAR(255), email VARCHAR(255), birth_date VARCHAR(10), pass VARCHAR(64), PRIMARY KEY(user_ID));
CREATE TABLE IF NOT EXISTS user_phone_number(user_ID INT REFERENCES users(user_ID), phone VARCHAR(10), PRIMARY KEY(user_ID, phone));
CREATE TABLE IF NOT EXISTS patient(patient_ID INT REFERENCES users(user_id), patient_age INT, PRIMARY KEY(patient_ID));
CREATE TABLE IF NOT EXISTS patient_medical_condition(patient_ID INT REFERENCES patient(patient_ID), medical_condition VARCHAR(100), PRIMARY KEY(patient_ID, medical_condition));
CREATE TABLE IF NOT EXISTS patient_insurance_plan(patient_ID INT REFERENCES patient(patient_ID), insurance_plan VARCHAR(100), PRIMARY KEY(patient_ID, insurance_plan));
CREATE TABLE IF NOT EXISTS physician(physician_ID INT REFERENCES users(user_id), avg_rating INT, specialty VARCHAR(50), PRIMARY KEY(physician_ID)); 
CREATE TABLE IF NOT EXISTS practice(reference_ID INT NOT NULL, practice_name VARCHAR(100), practice_location VARCHAR(255), established int, PRIMARY KEY(reference_ID));
CREATE TABLE IF NOT EXISTS practice_accepted_insurance(reference_ID INT REFERENCES practice(reference_ID), accepted_insurance VARCHAR(100), PRIMARY KEY(reference_ID, accepted_insurance));
CREATE TABLE IF NOT EXISTS works(physician_ID INT REFERENCES physician(physician_ID), reference_ID INT REFERENCES practice(reference_ID), PRIMARY KEY(physician_ID, reference_ID)); 
CREATE TABLE IF NOT EXISTS daily_check_in(check_in_number INT NOT NULL, date_taken VARCHAR(10), physical_wellness_rating INT, mental_wellness_rating INT, PRIMARY KEY(check_in_number));
CREATE TABLE IF NOT EXISTS assign(check_in_number INT REFERENCES daily_check_in(check_in_number), physician_ID INT REFERENCES physician(physician_ID), patient_ID INT REFERENCES patient(patient_ID), PRIMARY KEY(check_in_number, physician_ID, patient_ID));
CREATE TABLE IF NOT EXISTS appointment(appt_date_time VARCHAR(50), physician_ID INT REFERENCES physician(physician_ID), patient_ID INT REFERENCES patient(patient_ID), satisfaction_rating INT, physician_notes VARCHAR(300), location VARCHAR(255), PRIMARY KEY(appt_date_time, physician_ID, patient_ID));


